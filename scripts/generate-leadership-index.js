#!/usr/bin/env node
"use strict";

const fs = require("node:fs");
const path = require("node:path");
const {
  latestFileMatching,
  listTopicSlugs,
  readJsonIfExists,
  readTopicFrontmatter,
} = require("./lib/topic-utils");

const repoRoot = path.resolve(__dirname, "..");
const topicsRoot = path.join(repoRoot, "topics");
const outputPath = path.join(repoRoot, "topics", "index.md");

function firstNonEmpty(...values) {
  return values.find(value => value !== undefined && value !== null && String(value).trim() !== "") ?? "";
}

function extractVerdictSummary(slug) {
  const verdictPath = path.join(topicsRoot, slug, "verdict.md");
  if (!fs.existsSync(verdictPath)) return "";
  const text = fs.readFileSync(verdictPath, "utf8");
  const match = text.match(/## Recommendation\s*\r?\n+([\s\S]*?)(?=\r?\n##|\r?\n---|\s*$)/i);
  if (!match) return "";
  return match[1]
    .split(/\r?\n\r?\n/)[0]
    .replace(/\*\*/g, "")
    .replace(/\[([^\]]+)\]\([^)]+\)/g, "$1")
    .replace(/\s+/g, " ")
    .trim()
    .slice(0, 180);
}

function citationSummary(slug) {
  const pipelineDir = path.join(topicsRoot, slug, "_pipeline");
  const latest = latestFileMatching(pipelineDir, /^citation-check-\d{4}-\d{2}-\d{2}\.json$/);
  if (!latest) return "Not checked";
  const report = readJsonIfExists(latest);
  if (!report || report.__parseError) return "Invalid report";
  return `${report.ok ?? 0} OK / ${report.dead ?? 0} dead / ${report.error ?? 0} error`;
}

function stateFor(slug) {
  const state = readJsonIfExists(path.join(topicsRoot, slug, "_pipeline", "state.json"));
  return state && !state.__parseError ? state : {};
}

function securityStatus(state) {
  if (state.security_review_required === false) return "N/A";
  return firstNonEmpty(state.phases?.phase_5?.verdict, state.security_review_required ? "Required" : "Unknown");
}

function freshnessStatus(slug) {
  const verdict = readTopicFrontmatter(topicsRoot, slug, "verdict.md");
  return firstNonEmpty(verdict.updated, verdict.created, "Unknown");
}

function main() {
  const rows = [];
  for (const slug of listTopicSlugs(topicsRoot)) {
    const verdict = readTopicFrontmatter(topicsRoot, slug, "verdict.md");
    const overview = readTopicFrontmatter(topicsRoot, slug, "overview.md");
    const state = stateFor(slug);
    const title = firstNonEmpty(
      verdict.title?.replace(/\s*[—–-]\s*Verdict.*$/i, ""),
      overview.title,
      slug
    );
    const status = firstNonEmpty(verdict.status, overview.status, state.status, "Unknown");
    const score = firstNonEmpty(verdict.score, state.run_metrics?.final_score, state.phases?.phase_4?.final_score, "N/A");
    const confidence = firstNonEmpty(verdict.confidence, overview.confidence, "N/A");
    const workflow = firstNonEmpty(state.workflow, "Unknown");
    const citations = citationSummary(slug);
    const freshness = freshnessStatus(slug);
    const security = securityStatus(state);
    const summary = extractVerdictSummary(slug);
    rows.push(`| [${title}](${slug}/) | ${status} | ${workflow} | ${score} | ${confidence} | ${freshness} | ${citations} | ${security} | ${summary} |`);
  }

  const output = [
    "# Topic Index",
    "",
    "| Topic | Status | Workflow | Score | Confidence | Freshness | Citations | Security | Verdict |",
    "|---|---|---|---:|---|---|---|---|---|",
    ...rows,
    "",
    `*Generated by scripts/generate-leadership-index.js.*`,
    "",
  ].join("\n");

  fs.writeFileSync(outputPath, output, "utf8");
  console.log(`Leadership topic index written to ${path.relative(repoRoot, outputPath).replace(/\\/g, "/")}`);
}

main();
